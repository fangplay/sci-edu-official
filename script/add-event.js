$(function () {
        $("#check1").click(function () {
            if ($(this).is(":checked")) {
                $("#type_input1").removeAttr("disabled");
                $("#type_input1").focus();
            } else {
                $("#type_input1").attr("disabled", "disabled");
            }
        });
    });

